#include "libft.h"

void	ft_putnbr_fd(int nb, int fd)
{
	long int	nbr;

	nbr = (long int) nb;
	if (nbr < 0)
	{
		nbr *= -1;
		ft_putchar_fd('-', fd);
	}
	if (nbr > 9)
		ft_putnbr_fd(nbr / 10, fd);
	ft_putchar_fd((nbr % 10) + '0', fd);
}
